			<h1 class="entry-title">Our History</h1>
				<div class="entry-content">
					<p>In the beginning, 1944-1945, the Bethesda Community School was housed in a basement in the old Bethesda Elementary School, which was then located on Wilson Lane. At that time, BCS was funded by the Federal Government – the fund was called the Lanham Act and the monies were used all over the U.S. to provide Child Care Facilities for mothers who were going into the labor force in place of men called to war.
My Paternal Grandmother, Anne Trainer, joined the group of caregivers at BCS about three months after it began. At first, she only worked part time, but it soon grew to full time, and at the end of her first year she found herself the Director. </p>
<p>After peace was declared, the Lanham Act was terminated along with the funding. Anne decided to incorporate the school on a non-profit basis and moved to our current location which had previously been the very fashionable “Whitehall School” for girls.
My Mother, Jill Trainer, began teaching at BCS in 1965 when she and my father married, and became the Director when my Grandmother retired in 1979. Both my sister Teri and I attended BCS from 2 years of age through Kindergarten, and Teri is currently a teacher in the 3 year old class.</p>
<p>I went on to earn a degree in Early Childhood Education and began teaching at BCS in 1990. I spent 15 years in the classroom, and then moved into the Director position. My mother, Jill, is still involved in the administration of the school as well as teaching a class of 4 year olds, and both my children attended Bethesda Community School from 2 years of age through 5. The strong generational connection to BCS continues, as we have many current students whose parents attended our school.
I look forward to the opportunity to share our special and unique school with you,</p><p>Tammy Trainer</p>
